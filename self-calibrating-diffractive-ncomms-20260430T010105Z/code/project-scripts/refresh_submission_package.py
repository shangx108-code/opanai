def md5(path: Path) -> str:
    h = hashlib.md5()
    with path.open("rb") as f:
        for chunk in iter(lambda: f.read(8192), b""):
            h.update(chunk)
    return h.hexdigest()


def generate_md5_registry() -> None:
    targets = [
        ROUND6_SCRIPT,
        CONFIG_PATH,
        ROUND6_METRICS,
        ROUND6_SUMMARY_MD,
        ROUND6_SUMMARY_JSON,
        ROUND6_PANEL,
        ROUND6_HISTORY,
        RUN_LOG,
        SUBMISSION_DIR / "figure5_source_data.csv",
        SUBMISSION_DIR / "data_availability.md",
        SUBMISSION_DIR / "code_availability.md",
        SUBMISSION_DIR / "methods_reproducibility.md",
        SUBMISSION_DIR / "reproducibility_registry.csv",
    ]
    rows = [
        {"path": str(path), "file_name": path.name, "md5": md5(path), "size_bytes": path.stat().st_size}
        for path in targets
    ]
    write_csv(rows, SUBMISSION_DIR / "md5_registry.csv")


def main() -> None:
    ensure_dirs()
    metrics_rows = read_metrics()
    summary = read_summary()
    generate_source_data(metrics_rows, summary)
    generate_data_availability()
    generate_code_availability()
    generate_methods_reproducibility(summary)
    generate_registry(summary)
    generate_md5_registry()


if __name__ == "__main__":
    main()
