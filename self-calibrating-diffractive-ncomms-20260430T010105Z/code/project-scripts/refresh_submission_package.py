from __future__ import annotations

import csv
import hashlib
import json
from pathlib import Path


PROJECT_ROOT = Path(__file__).resolve().parents[2]
OUTPUT_DIR = PROJECT_ROOT / "artifacts" / "project-outputs"
SUBMISSION_DIR = PROJECT_ROOT / "artifacts" / "submission-package"
CONFIG_PATH = PROJECT_ROOT / "code" / "config" / "round6_config_template.json"
ROUND6_SCRIPT = PROJECT_ROOT / "code" / "project-scripts" / "round6_numpy_passive_d2nn.py"
ROUND6_METRICS = OUTPUT_DIR / "round6_numpy_passive_d2nn_metrics.csv"
ROUND6_SUMMARY_JSON = OUTPUT_DIR / "round6_numpy_passive_d2nn_summary.json"
ROUND6_SUMMARY_MD = OUTPUT_DIR / "round6_numpy_passive_d2nn_summary.md"
ROUND6_PANEL = OUTPUT_DIR / "round6_numpy_passive_d2nn_panel.png"
ROUND6_HISTORY = OUTPUT_DIR / "round6_numpy_passive_d2nn_training_history.json"
RUN_LOG = PROJECT_ROOT / "logs" / "round6_rerun_20260430T010105Z.md"


def ensure_dirs() -> None:
    SUBMISSION_DIR.mkdir(parents=True, exist_ok=True)


def read_metrics() -> list[dict[str, str]]:
    with ROUND6_METRICS.open(newline="", encoding="utf-8") as f:
        return list(csv.DictReader(f))


def read_summary() -> dict:
    return json.loads(ROUND6_SUMMARY_JSON.read_text(encoding="utf-8"))


def write_csv(rows: list[dict[str, object]], target: Path) -> None:
    with target.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=list(rows[0].keys()))
        writer.writeheader()
        writer.writerows(rows)


def generate_source_data(metrics_rows: list[dict[str, str]], summary: dict) -> None:
    rows: list[dict[str, object]] = []
    for row in metrics_rows:
        rows.append(
            {
                "table_section": "raw_metrics",
                "split": row["split"],
                "sample": row["sample"],
                "condition": row["condition"],
                "psnr_db": row["psnr_db"],
                "mse": row["mse"],
                "ood_mean_psnr_db": "",
                "ood_mean_mse": "",
                "train_final_mse": "",
                "comparison": "",
                "delta_db": "",
            }
        )

    for row in summary["summary_rows"]:
        rows.append(
            {
                "table_section": "condition_summary",
                "split": "ood",
                "sample": "mean",
                "condition": row["condition"],
                "psnr_db": "",
                "mse": "",
                "ood_mean_psnr_db": f'{row["ood_mean_psnr_db"]:.6f}',
                "ood_mean_mse": f'{row["ood_mean_mse"]:.8f}',
                "train_final_mse": f'{row["train_final_mse"]:.8f}',
                "comparison": "",
                "delta_db": "",
            }
        )

    for name, value in summary["deltas_db"].items():
        rows.append(
            {
                "table_section": "delta_summary",
                "split": "ood",
                "sample": "mean",
                "condition": "common_path",
                "psnr_db": "",
                "mse": "",
                "ood_mean_psnr_db": "",
                "ood_mean_mse": "",
                "train_final_mse": "",
                "comparison": name.replace("common_minus_", ""),
                "delta_db": f"{value:.6f}",
            }
        )

    write_csv(rows, SUBMISSION_DIR / "figure5_source_data.csv")


